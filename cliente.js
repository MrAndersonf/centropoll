function handleClientes() {
    let tabela = document.getElementById('table-clientes');
    let lista = [];
    file.forEach((cliente, index) => {
        let produtoString = `<tr>
                                <th scope="row">${index}</th>
                                <td>${cliente.nome}</td>                              
                                <td>${cliente.cpf}</td>
                                <td>
                                    <div>
                                        <button class="btn">
                                        <img src="./icons/edit.svg"/>
                                        </button>
                                        <button class="btn">
                                        <img src="./icons/delete.svg"/>
                                        </button>
                                        <button class="btn" style="color: green" onclick="handleFazendas('${cliente.nome}')">
                                        <img src="./icons/map.svg"/>
                                        </button>
                                    </div>
                                </td>
                            </tr>`
        lista.push(produtoString)

    })
    tabela.innerHTML = lista.join('')

}


function handleGrupos() {
    let grupos = document.getElementById('lista-grupos');
    let lista = []
    file.forEach(produto => {
        if (!lista.includes(produto.grupo)) {
            lista.push(produto.grupo)
        }
    })
    lista.sort()
    grupos.innerHTML = lista.map(el => `<option value="${el}">${el}</option>`)
}


function handleFazendas(produtor) {
    let tabela = document.getElementById('table-propriedades');
    const propriedades = fazendas.filter(propriedade => propriedade.proprietario === produtor)
    const rows = propriedades.map((el, index) => `<tr><th scope="row">${index}</th>
    <td>${el.propriedade}</td>                              
    <td>${el.municipio}</td>
    <td>${el.inscricao}</td>
    <td><div>
            <button class="btn">
                <img src="./icons/edit.svg"/>
            </button>
            <button class="btn">
            <img src="./icons/delete.svg"/>
            </button>
            
</div></td></tr>`)
    tabela.innerHTML = rows.join('')
}

